public class TiposVariaveis {
  public static void main(String[] args) {
    int myNum = 5;               // integer (whole number)
    float myFloatNum = 5.99f;    // floating point number
    char myLetter = 'D';         // character
    boolean myBool = true;       // boolean
    String myText = "Hello";     // String    
    System.out.println(myNum);
    System.out.println(myFloatNum);
    System.out.println(myLetter);
    System.out.println(myBool);
    System.out.println(myText);


      byte myNumMini = 100;         // Números de -128 a 127
    System.out.println(myNumMini);  

        short myNumShort = 5000;       // Números de -32768 a 32767
    System.out.println(myNumShort);  

        int myNumInt = 100000;       // Números de -2147483648 a 2147483647
    System.out.println(myNumInt);  

        long myNumLong = 15000000000L;   // Números de -9223372036854775808 a 9223372036854775807
    System.out.println(myNumLong);  

        float myNumFloat = 5.75f;          // Números decimais exatos
    System.out.println(myNumFloat);

        double myNumD = 19.99d;
     System.out.println(myNumD);

     
        float f1 = 35e3f;        // O 'e' coloca a quantidade de 0 que vc colocou na frente do próprio 'e'
        double d1 = 12E4d;
     System.out.println(f1);
     System.out.println(d1);
  }
}
